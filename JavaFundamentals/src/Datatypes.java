
public class Datatypes {
	public static void main(String[] args) {
		//Datatypes in java.
		 // Total 8 datatypes available in java.
		  
		 byte b=5;
		 System.out.println(b);
		 short s=10;
		 System.out.println(s);
		 int i=10;
		 System.out.println(i);
		 long l=2000;
		 System.out.println(l);
		  
		  float f=5f;////4byte 32 bits is used to store the numbers.
		  System.out.println(f);
		  double d=3d;
		  System.out.println(d);//8 byte 64 bits is used to store the numbers.
		  char c='a';//2byte
		  System.out.println(c);
		  boolean result=true;
		  System.out.println(result);
	}

}
